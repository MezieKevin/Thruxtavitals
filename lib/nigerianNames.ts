/** Number of unique full-name combinations (first × last). */
export const NIGERIAN_FULL_NAME_COMBO_COUNT =
  NIGERIAN_FIRST_NAMES.length * NIGERIAN_SURNAMES.length;

export function pickRandomNigerianFullName(): string {
  const fi = Math.floor(Math.random() * NIGERIAN_FIRST_NAMES.length);
  const li = Math.floor(Math.random() * NIGERIAN_SURNAMES.length);
  return `${NIGERIAN_FIRST_NAMES[fi]} ${NIGERIAN_SURNAMES[li]}`;
}
